    I was able to develop a resilience to apathy and the ability to articulate myself in front of large groups or in one-on-one conversations with strangers.
</p>

<h3>
    Senior Student Ambassador
    <span class="pull-right">
        2012/2013
    </span>
</h3>

<hr>

<p>
    As a Senior Ambassador of the UBC Campus Tour programs, I acquired valuable experience in navigating the development of friendships with my peers while interacting with them in a managerial position.
    I also was exposed to an office environment, something that will lend itself to my ability to thrive in the culture of the Executive offices.
</p>

<h3>
    UBC BikeRave Co-Founder
    <span class="pull-right">
        2013/2014
    </span>
</h3>

<hr>

<p>
    Responding to the lack of fun, eccentric events being held on campus, I decided to bring the concept of a BikeRave – a giant neon dance party on wheels – to the UBC community in the hopes of celebrating UBC bike culture and promoting sustainable transportation.
    Through the organization of two wildly successful UBC BikeRave events, I gained valuable experience in taking an abstract idea and working to build a team to make it a reality over time.
    Experiences such as this one demonstrate my ability to tackle large initiatives and direct progress towards a common goal on a team project.
</p>

<h3>
    Science Undergraduate Society President
    <span class="pull-right">
        2013/2014
    </span>
</h3>

<hr>

<p>
    My time with the Science Undergraduate Society affirmed my passion for student government, and provided the training ground for developing many of my political skills.
    Most prominently, however, my time as President taught me the importance of developing a positive team dynamic with the understanding that people work with different styles.
    I developed the ability to compromise and adapt my leadership style to different team members in this role, which eventually led to SUS winning the AMS Best Constituency Award during my term.
</p>

<h3>
    AMS Microbrewery Referendum Lead
    <span class="pull-right">
        2013/2014
    </span>
</h3>

<hr>

<p>
    During the 2014 AMS Election period, I led the Microbrewery referendum campaign that included collecting over 1700 petition signatures in a single week, and developing a comprehensive marketing campaign to draw out student voters.
    This experience reaffirmed to me the idea that projects undertaken by the AMS must originate from the wants and needs of the student body, and that the priority of the majority is paramount to the direction that the Executive wishes to pursue.
</p>

<h3>
    AMS Oversight Committee Chair
    <span class="pull-right">
        2013/2014
    </span>
</h3>

<hr>

<p>
    During my first year on AMS Council, I served as the Chair of the Oversight Committee responsible for adjudicating the Executive Performance Accountability Incentive.
    I was able to develop a process and rubric for evaluation that did not exist currently in order to more accurately assess Executive performance.
    Easily my most challenging involvement at UBC, this experience taught me the intricacies and difficulties surrounding conflict resolution, and provided me with the confidence to work effectively in high pressure situations.
</p>

<h3>
